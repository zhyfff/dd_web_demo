package Hisign.entity;

public class CourierInfor {
	private String dcnumber;//单据号
	private String recipients;//收件人
	private String express_no;//快递号
	private String notice_time;//通知时间
	private String pay_type;//是否到付
	private String notice_word;//通知语
	private String lq;//领取人
	private String express;//快递公司
	private String note;//备注
	private String evaluation;//服务评价
	private String overtime;//结束时间
	public String getDcnumber() {
		return dcnumber;
	}
	public void setDcnumber(String dcnumber) {
		this.dcnumber = dcnumber;
	}
	public String getRecipients() {
		return recipients;
	}
	public void setRecipients(String recipients) {
		this.recipients = recipients;
	}
	public String getExpress_no() {
		return express_no;
	}
	public void setExpress_no(String express_no) {
		this.express_no = express_no;
	}
	public String getNotice_time() {
		return notice_time;
	}
	public void setNotice_time(String notice_time) {
		this.notice_time = notice_time;
	}
	public String getPay_type() {
		return pay_type;
	}
	public void setPay_type(String pay_type) {
		this.pay_type = pay_type;
	}
	public String getNotice_word() {
		return notice_word;
	}
	public void setNotice_word(String notice_word) {
		this.notice_word = notice_word;
	}
	public String getLq() {
		return lq;
	}
	public void setLq(String lq) {
		this.lq = lq;
	}
	public String getExpress() {
		return express;
	}
	public void setExpress(String express) {
		this.express = express;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getEvaluation() {
		return evaluation;
	}
	public void setEvaluation(String evaluation) {
		this.evaluation = evaluation;
	}
	public String getOvertime() {
		return overtime;
	}
	public void setOvertime(String overtime) {
		this.overtime = overtime;
	}
	
	
	

}
